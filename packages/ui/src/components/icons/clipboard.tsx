"use client";

import { cn } from "@foundry/ui/lib/utils";
import type { Variants } from "motion/react";
import { motion, useAnimation } from "motion/react";
import type { HTMLAttributes } from "react";
import { forwardRef, useCallback, useImperativeHandle, useRef } from "react";

export interface ClipboardIconHandle {
    startAnimation: () => void;
    stopAnimation: () => void;
}

interface ClipboardIconProps extends HTMLAttributes<HTMLDivElement> {
    size?: number;
}

const CLIP_VARIANTS: Variants = {
    normal: { scaleY: 1, y: 0 },
    animate: {
        scaleY: [1, 0.8, 1],
        y: [0, 0.5, 0],
        transition: {
            duration: 0.3,
            ease: "easeInOut",
        },
    },
};

const BOARD_VARIANTS: Variants = {
    normal: { y: 0 },
    animate: {
        y: [0, -0.5, 0],
        transition: {
            duration: 0.3,
            ease: "easeInOut",
        },
    },
};

const ClipboardIcon = forwardRef<ClipboardIconHandle, ClipboardIconProps>(({ onMouseEnter, onMouseLeave, className, size = 20, ...props }, ref) => {
    const controls = useAnimation();
    const isControlledRef = useRef(false);

    useImperativeHandle(ref, () => {
        isControlledRef.current = true;

        return {
            startAnimation: () => controls.start("animate"),
            stopAnimation: () => controls.start("normal"),
        };
    });

    const handleMouseEnter = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseEnter?.(e);
            } else {
                controls.start("animate");
            }
        },
        [controls, onMouseEnter]
    );

    const handleMouseLeave = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseLeave?.(e);
            } else {
                controls.start("normal");
            }
        },
        [controls, onMouseLeave]
    );

    return (
        <div className={cn(className)} onMouseEnter={handleMouseEnter} onMouseLeave={handleMouseLeave} {...props}>
            <svg
                fill="none"
                height={size}
                stroke="currentColor"
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth="1.5"
                viewBox="0 0 24 24"
                width={size}
                xmlns="http://www.w3.org/2000/svg"
            >
                <motion.path
                    animate={controls}
                    d="M15.6657 3.88789C15.3991 2.94272 14.5305 2.25 13.5 2.25H10.5C9.46954 2.25 8.60087 2.94272 8.33426 3.88789M15.6657 3.88789C15.7206 4.0825 15.75 4.28782 15.75 4.5V4.5C15.75 4.91421 15.4142 5.25 15 5.25H9C8.58579 5.25 8.25 4.91421 8.25 4.5V4.5C8.25 4.28782 8.27937 4.0825 8.33426 3.88789"
                    initial="normal"
                    style={{ originY: 0 }}
                    variants={CLIP_VARIANTS}
                />
                <motion.path
                    animate={controls}
                    d="M15.6657 3.88789C16.3119 3.93668 16.9545 3.99828 17.5933 4.07241C18.6939 4.20014 19.5 5.149 19.5 6.25699V19.5C19.5 20.7426 18.4926 21.75 17.25 21.75H6.75C5.50736 21.75 4.5 20.7426 4.5 19.5V6.25699C4.5 5.149 5.30608 4.20014 6.40668 4.07241C7.04547 3.99828 7.68808 3.93668 8.33426 3.88789"
                    initial="normal"
                    variants={BOARD_VARIANTS}
                />
            </svg>
        </div>
    );
});

ClipboardIcon.displayName = "ClipboardIcon";

export { ClipboardIcon };
