"use client";

import { cn } from "@foundry/ui/lib/utils";
import type { Variants } from "motion/react";
import { motion, useAnimation } from "motion/react";
import type { HTMLAttributes } from "react";
import { forwardRef, useCallback, useImperativeHandle, useRef } from "react";

export interface PhoneArrowDownLeftIconHandle {
    startAnimation: () => void;
    stopAnimation: () => void;
}

interface PhoneArrowDownLeftIconProps extends HTMLAttributes<HTMLDivElement> {
    size?: number;
}

const ARROW_VARIANTS: Variants = {
    normal: { translateX: 0, translateY: 0 },
    animate: {
        translateX: [0, -3, 0],
        translateY: [0, 3, 0],
        transition: {
            duration: 0.5,
            ease: "easeInOut",
        },
    },
};

const PhoneArrowDownLeftIcon = forwardRef<PhoneArrowDownLeftIconHandle, PhoneArrowDownLeftIconProps>(({ onMouseEnter, onMouseLeave, className, size = 20, ...props }, ref) => {
    const controls = useAnimation();
    const isControlledRef = useRef(false);

    useImperativeHandle(ref, () => {
        isControlledRef.current = true;

        return {
            startAnimation: () => {
                controls.start("animate");
            },
            stopAnimation: () => {
                controls.start("normal");
            },
        };
    });

    const handleMouseEnter = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseEnter?.(e);
            } else {
                controls.start("animate");
            }
        },
        [controls, onMouseEnter]
    );

    const handleMouseLeave = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseLeave?.(e);
            } else {
                controls.start("normal");
            }
        },
        [controls, onMouseLeave]
    );

    return (
        <div className={cn(className)} onMouseEnter={handleMouseEnter} onMouseLeave={handleMouseLeave} {...props}>
            <svg
                fill="none"
                height={size}
                stroke="currentColor"
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth="1.5"
                viewBox="0 0 24 24"
                width={size}
                xmlns="http://www.w3.org/2000/svg"
            >
                <path d="M17.25 21.75c-8.284 0-15-6.716-15-15V4.5A2.25 2.25 0 0 1 4.5 2.25h1.372c.516 0 .966.351 1.091.852l1.106 4.423c.11.44-.054.902-.417 1.173l-1.293.97a1.062 1.062 0 0 0-.38 1.21 12.035 12.035 0 0 0 7.143 7.143c.441.162.928-.004 1.21-.38l.97-1.293a1.125 1.125 0 0 1 1.173-.417l4.423 1.106c.5.125.852.575.852 1.091V19.5a2.25 2.25 0 0 1-2.25 2.25h-2.25Z" />
                <motion.path animate={controls} d="M14.25 9.75v-4.5m0 4.5h4.5m-4.5 0 6-6" initial="normal" variants={ARROW_VARIANTS} />
            </svg>
        </div>
    );
});

PhoneArrowDownLeftIcon.displayName = "PhoneArrowDownLeftIcon";

export { PhoneArrowDownLeftIcon };
