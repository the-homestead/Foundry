"use client";

import { cn } from "@foundry/ui/lib/utils";
import type { Variants } from "motion/react";
import { motion, useAnimation } from "motion/react";
import type { HTMLAttributes } from "react";
import { forwardRef, useCallback, useImperativeHandle, useRef } from "react";

export interface ChatBubbleBottomCenterIconHandle {
    startAnimation: () => void;
    stopAnimation: () => void;
}

interface ChatBubbleBottomCenterIconProps extends HTMLAttributes<HTMLDivElement> {
    size?: number;
}

const ICON_VARIANTS: Variants = {
    normal: {
        scale: 1,
        rotate: 0,
    },
    animate: {
        scale: 1.05,
        rotate: [0, -7, 7, 0],
        transition: {
            rotate: {
                duration: 0.5,
                ease: "easeInOut",
            },
            scale: {
                type: "spring",
                stiffness: 400,
                damping: 10,
            },
        },
    },
};

const ChatBubbleBottomCenterIcon = forwardRef<ChatBubbleBottomCenterIconHandle, ChatBubbleBottomCenterIconProps>(
    ({ onMouseEnter, onMouseLeave, className, size = 20, ...props }, ref) => {
        const controls = useAnimation();
        const isControlledRef = useRef(false);

        useImperativeHandle(ref, () => {
            isControlledRef.current = true;

            return {
                startAnimation: () => controls.start("animate"),
                stopAnimation: () => controls.start("normal"),
            };
        });

        const handleMouseEnter = useCallback(
            (e: React.MouseEvent<HTMLDivElement>) => {
                if (isControlledRef.current) {
                    onMouseEnter?.(e);
                } else {
                    controls.start("animate");
                }
            },
            [controls, onMouseEnter]
        );

        const handleMouseLeave = useCallback(
            (e: React.MouseEvent<HTMLDivElement>) => {
                if (isControlledRef.current) {
                    onMouseLeave?.(e);
                } else {
                    controls.start("normal");
                }
            },
            [controls, onMouseLeave]
        );

        return (
            <div className={cn(className)} onMouseEnter={handleMouseEnter} onMouseLeave={handleMouseLeave} {...props}>
                <motion.svg
                    animate={controls}
                    fill="none"
                    height={size}
                    stroke="currentColor"
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    strokeWidth="1.5"
                    variants={ICON_VARIANTS}
                    viewBox="0 0 24 24"
                    width={size}
                    xmlns="http://www.w3.org/2000/svg"
                >
                    <path d="M2.25 12.76c0 1.6 1.123 2.994 2.707 3.227 1.068.157 2.148.279 3.238.364.466.037.893.281 1.153.671L12 21l2.652-3.978c.26-.39.687-.634 1.153-.67 1.09-.086 2.17-.208 3.238-.365 1.584-.233 2.707-1.626 2.707-3.228V6.741c0-1.602-1.123-2.995-2.707-3.228A48.394 48.394 0 0 0 12 3c-2.392 0-4.744.175-7.043.513C3.373 3.746 2.25 5.14 2.25 6.741v6.018Z" />
                </motion.svg>
            </div>
        );
    }
);

ChatBubbleBottomCenterIcon.displayName = "ChatBubbleBottomCenterIcon";

export { ChatBubbleBottomCenterIcon };
