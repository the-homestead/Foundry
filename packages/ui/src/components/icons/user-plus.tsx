"use client";

import { cn } from "@foundry/ui/lib/utils";
import type { Variants } from "motion/react";
import { motion, useAnimation } from "motion/react";
import type { HTMLAttributes } from "react";
import { forwardRef, useCallback, useImperativeHandle, useRef } from "react";

export interface UserPlusIconHandle {
    startAnimation: () => void;
    stopAnimation: () => void;
}

interface UserPlusIconProps extends HTMLAttributes<HTMLDivElement> {
    size?: number;
}

const VERTICAL_LINE_VARIANT: Variants = {
    normal: {
        opacity: 1,
    },
    animate: {
        opacity: [0, 1],
        pathLength: [0, 1],
        transition: {
            delay: 0.3,
            duration: 0.2,
            opacity: { duration: 0.1, delay: 0.3 },
        },
    },
};

const HORIZONTAL_LINE_VARIANT: Variants = {
    normal: {
        opacity: 1,
    },
    animate: {
        opacity: [0, 1],
        pathLength: [0, 1],
        transition: {
            delay: 0.6,
            duration: 0.2,
            opacity: { duration: 0.1, delay: 0.6 },
        },
    },
};

const UserPlusIcon = forwardRef<UserPlusIconHandle, UserPlusIconProps>(({ onMouseEnter, onMouseLeave, className, size = 20, ...props }, ref) => {
    const controls = useAnimation();
    const isControlledRef = useRef(false);

    useImperativeHandle(ref, () => {
        isControlledRef.current = true;

        return {
            startAnimation: () => controls.start("animate"),
            stopAnimation: () => controls.start("normal"),
        };
    });

    const handleMouseEnter = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseEnter?.(e);
            } else {
                controls.start("animate");
            }
        },
        [controls, onMouseEnter]
    );

    const handleMouseLeave = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseLeave?.(e);
            } else {
                controls.start("normal");
            }
        },
        [controls, onMouseLeave]
    );

    return (
        <div className={cn(className)} onMouseEnter={handleMouseEnter} onMouseLeave={handleMouseLeave} {...props}>
            <svg
                fill="none"
                height={size}
                stroke="currentColor"
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth="1.5"
                viewBox="0 0 24 24"
                width={size}
                xmlns="http://www.w3.org/2000/svg"
            >
                <path d="M13.75 6.375a3.375 3.375 0 1 1-6.75 0 3.375 3.375 0 0 1 6.75 0ZM3 19.235v-.11a6.375 6.375 0 0 1 12.75 0v.109A12.318 12.318 0 0 1 9.374 21c-2.331 0-4.512-.645-6.374-1.766Z" />
                <motion.path animate={controls} d="M18 7.5v6" initial="normal" variants={VERTICAL_LINE_VARIANT} />
                <motion.path animate={controls} d="M15 10.5h6" initial="normal" variants={HORIZONTAL_LINE_VARIANT} />
            </svg>
        </div>
    );
});

UserPlusIcon.displayName = "UserPlusIcon";

export { UserPlusIcon };
