"use client";

import { cn } from "@foundry/ui/lib/utils";
import type { Variants } from "motion/react";
import { motion, useAnimation } from "motion/react";
import type { HTMLAttributes } from "react";
import { forwardRef, useCallback, useImperativeHandle, useRef } from "react";

export interface PresentationChartBarIconHandle {
    startAnimation: () => void;
    stopAnimation: () => void;
}

interface PresentationChartBarIconProps extends HTMLAttributes<HTMLDivElement> {
    size?: number;
}

const CREATE_BAR_VARIANTS = (delay: number): Variants => ({
    normal: {
        opacity: 1,
        pathLength: 1,
        pathOffset: 0,
        transition: {
            duration: 0.4,
            opacity: { duration: 0.1 },
        },
    },
    animate: {
        opacity: [0, 1],
        pathLength: [0, 1],
        pathOffset: [1, 0],
        transition: {
            delay,
            duration: 0.4,
            ease: "easeOut",
            opacity: { duration: 0.1, delay },
        },
    },
});

const PresentationChartBarIcon = forwardRef<PresentationChartBarIconHandle, PresentationChartBarIconProps>(
    ({ onMouseEnter, onMouseLeave, className, size = 28, ...props }, ref) => {
        const controls = useAnimation();
        const isControlledRef = useRef(false);

        useImperativeHandle(ref, () => {
            isControlledRef.current = true;

            return {
                startAnimation: () => controls.start("animate"),
                stopAnimation: () => controls.start("normal"),
            };
        });

        const handleMouseEnter = useCallback(
            (e: React.MouseEvent<HTMLDivElement>) => {
                if (isControlledRef.current) {
                    onMouseEnter?.(e);
                } else {
                    controls.start("animate");
                }
            },
            [controls, onMouseEnter]
        );

        const handleMouseLeave = useCallback(
            (e: React.MouseEvent<HTMLDivElement>) => {
                if (isControlledRef.current) {
                    onMouseLeave?.(e);
                } else {
                    controls.start("normal");
                }
            },
            [controls, onMouseLeave]
        );

        return (
            <div className={cn(className)} onMouseEnter={handleMouseEnter} onMouseLeave={handleMouseLeave} {...props}>
                <svg
                    fill="none"
                    height={size}
                    stroke="currentColor"
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    strokeWidth="1.5"
                    viewBox="0 0 24 24"
                    width={size}
                    xmlns="http://www.w3.org/2000/svg"
                >
                    <path d="M3.75 3v11.25A2.25 2.25 0 0 0 6 16.5h2.25M3.75 3h-1.5m1.5 0h16.5m0 0h1.5m-1.5 0v11.25A2.25 2.25 0 0 1 18 16.5h-2.25m-7.5 0h7.5m-7.5 0-1 3m8.5-3 1 3m0 0 .5 1.5m-.5-1.5h-9.5m0 0-.5 1.5" />
                    <motion.path animate={controls} d="M9 11.25v1.5" initial="normal" variants={CREATE_BAR_VARIANTS(0)} />
                    <motion.path animate={controls} d="M12 9v3.75" initial="normal" variants={CREATE_BAR_VARIANTS(0.15)} />
                    <motion.path animate={controls} d="M15 6.75v6" initial="normal" variants={CREATE_BAR_VARIANTS(0.3)} />
                </svg>
            </div>
        );
    }
);

PresentationChartBarIcon.displayName = "PresentationChartBarIcon";

export { PresentationChartBarIcon };
