"use client";

import { cn } from "@foundry/ui/lib/utils";
import type { Variants } from "motion/react";
import { motion, useAnimation } from "motion/react";
import type { HTMLAttributes } from "react";
import { forwardRef, useCallback, useImperativeHandle, useRef } from "react";

export interface AcademicCapIconHandle {
    startAnimation: () => void;
    stopAnimation: () => void;
}

interface AcademicCapIconProps extends HTMLAttributes<HTMLDivElement> {
    size?: number;
}

const CAP_VARIANTS: Variants = {
    normal: { y: 0, rotate: 0 },
    animate: {
        y: [0, -3, 0],
        rotate: [0, -5, 5, 0],
        transition: {
            duration: 0.5,
            ease: "easeInOut",
        },
    },
};

const TASSEL_VARIANTS: Variants = {
    normal: { rotate: 0 },
    animate: {
        rotate: [0, 10, -10, 5, 0],
        transition: {
            duration: 0.6,
            ease: "easeInOut",
        },
    },
};

const AcademicCapIcon = forwardRef<AcademicCapIconHandle, AcademicCapIconProps>(({ onMouseEnter, onMouseLeave, className, size = 28, ...props }, ref) => {
    const controls = useAnimation();
    const isControlledRef = useRef(false);

    useImperativeHandle(ref, () => {
        isControlledRef.current = true;

        return {
            startAnimation: () => controls.start("animate"),
            stopAnimation: () => controls.start("normal"),
        };
    });

    const handleMouseEnter = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseEnter?.(e);
            } else {
                controls.start("animate");
            }
        },
        [controls, onMouseEnter]
    );

    const handleMouseLeave = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseLeave?.(e);
            } else {
                controls.start("normal");
            }
        },
        [controls, onMouseLeave]
    );

    return (
        <div className={cn(className)} onMouseEnter={handleMouseEnter} onMouseLeave={handleMouseLeave} {...props}>
            <svg
                fill="none"
                height={size}
                stroke="currentColor"
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth="1.5"
                viewBox="0 0 24 24"
                width={size}
                xmlns="http://www.w3.org/2000/svg"
            >
                <motion.g animate={controls} initial="normal" style={{ transformOrigin: "center center" }} variants={CAP_VARIANTS}>
                    <path d="M4.26 10.147a60.438 60.438 0 0 0-.491 6.347A48.62 48.62 0 0 1 12 20.904a48.62 48.62 0 0 1 8.232-4.41 60.46 60.46 0 0 0-.491-6.347m-15.482 0a50.636 50.636 0 0 0-2.658-.813A59.906 59.906 0 0 1 12 3.493a59.903 59.903 0 0 1 10.399 5.84c-.896.248-1.783.52-2.658.814m-15.482 0A50.717 50.717 0 0 1 12 13.489a50.702 50.702 0 0 1 7.74-3.342" />
                </motion.g>
                <motion.g animate={controls} initial="normal" style={{ transformOrigin: "6.75px 14.25px" }} variants={TASSEL_VARIANTS}>
                    <path d="M6.75 15a.75.75 0 1 0 0-1.5.75.75 0 0 0 0 1.5Zm0 0v-3.675A55.378 55.378 0 0 1 12 8.443m-7.007 11.55A5.981 5.981 0 0 0 6.75 15.75v-1.5" />
                </motion.g>
            </svg>
        </div>
    );
});

AcademicCapIcon.displayName = "AcademicCapIcon";

export { AcademicCapIcon };
