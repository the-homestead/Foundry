"use client";

import { cn } from "@foundry/ui/lib/utils";
import type { Variants } from "motion/react";
import { motion, useAnimation } from "motion/react";
import type { HTMLAttributes } from "react";
import { forwardRef, useCallback, useImperativeHandle, useRef } from "react";

export interface ReceiptPercentIconHandle {
    startAnimation: () => void;
    stopAnimation: () => void;
}

interface ReceiptPercentIconProps extends HTMLAttributes<HTMLDivElement> {
    size?: number;
}

const PERCENT_VARIANTS: Variants = {
    normal: {
        opacity: 1,
        pathLength: 1,
    },
    animate: {
        opacity: [0, 1],
        pathLength: [0, 1],
        transition: {
            duration: 0.4,
            ease: "easeOut",
            opacity: { duration: 0.1 },
        },
    },
};

const DOT_VARIANTS: Variants = {
    normal: {
        scale: 1,
        opacity: 1,
    },
    animate: (delay: number) => ({
        scale: [0, 1.2, 1],
        opacity: [0, 1, 1],
        transition: {
            delay,
            duration: 0.3,
            ease: "easeOut",
        },
    }),
};

const ReceiptPercentIcon = forwardRef<ReceiptPercentIconHandle, ReceiptPercentIconProps>(({ onMouseEnter, onMouseLeave, className, size = 28, ...props }, ref) => {
    const controls = useAnimation();
    const isControlledRef = useRef(false);

    useImperativeHandle(ref, () => {
        isControlledRef.current = true;

        return {
            startAnimation: () => controls.start("animate"),
            stopAnimation: () => controls.start("normal"),
        };
    });

    const handleMouseEnter = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseEnter?.(e);
            } else {
                controls.start("animate");
            }
        },
        [controls, onMouseEnter]
    );

    const handleMouseLeave = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseLeave?.(e);
            } else {
                controls.start("normal");
            }
        },
        [controls, onMouseLeave]
    );

    return (
        <div className={cn(className)} onMouseEnter={handleMouseEnter} onMouseLeave={handleMouseLeave} {...props}>
            <svg
                fill="none"
                height={size}
                stroke="currentColor"
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth="1.5"
                viewBox="0 0 24 24"
                width={size}
                xmlns="http://www.w3.org/2000/svg"
            >
                <path d="M19.5 4.75699V21.75L15.75 20.25L12 21.75L8.25 20.25L4.5 21.75V4.75699C4.5 3.649 5.30608 2.70014 6.40668 2.57241C8.24156 2.35947 10.108 2.25 12 2.25C13.892 2.25 15.7584 2.35947 17.5933 2.57241C18.6939 2.70014 19.5 3.649 19.5 4.75699Z" />
                <motion.path animate={controls} d="M9 14.25L15 8.25" initial="normal" variants={PERCENT_VARIANTS} />
                <motion.path
                    animate={controls}
                    custom={0.2}
                    d="M9.75 9H9.7575V9.0075H9.75V9ZM10.125 9C10.125 9.20711 9.95711 9.375 9.75 9.375C9.54289 9.375 9.375 9.20711 9.375 9C9.375 8.79289 9.54289 8.625 9.75 8.625C9.95711 8.625 10.125 8.79289 10.125 9Z"
                    initial="normal"
                    variants={DOT_VARIANTS}
                />
                <motion.path
                    animate={controls}
                    custom={0.35}
                    d="M14.25 13.5H14.2575V13.5075H14.25V13.5ZM14.625 13.5C14.625 13.7071 14.4571 13.875 14.25 13.875C14.0429 13.875 13.875 13.7071 13.875 13.5C13.875 13.2929 14.0429 13.125 14.25 13.125C14.4571 13.125 14.625 13.2929 14.625 13.5Z"
                    initial="normal"
                    variants={DOT_VARIANTS}
                />
            </svg>
        </div>
    );
});

ReceiptPercentIcon.displayName = "ReceiptPercentIcon";

export { ReceiptPercentIcon };
