"use client";

import { cn } from "@foundry/ui/lib/utils";
import type { Variants } from "motion/react";
import { motion, useAnimation } from "motion/react";
import type { HTMLAttributes } from "react";
import { forwardRef, useCallback, useImperativeHandle, useRef } from "react";

export interface SquaresPlusIconHandle {
    startAnimation: () => void;
    stopAnimation: () => void;
}

interface SquaresPlusIconProps extends HTMLAttributes<HTMLDivElement> {
    size?: number;
}

const VERTICAL_LINE_VARIANT: Variants = {
    normal: {
        opacity: 1,
    },
    animate: {
        opacity: [0, 1],
        pathLength: [0, 1],
        transition: {
            delay: 0.3,
            duration: 0.2,
            opacity: { duration: 0.1, delay: 0.3 },
        },
    },
};

const HORIZONTAL_LINE_VARIANT: Variants = {
    normal: {
        opacity: 1,
    },
    animate: {
        opacity: [0, 1],
        pathLength: [0, 1],
        transition: {
            delay: 0.6,
            duration: 0.2,
            opacity: { duration: 0.1, delay: 0.6 },
        },
    },
};

const SquaresPlusIcon = forwardRef<SquaresPlusIconHandle, SquaresPlusIconProps>(({ onMouseEnter, onMouseLeave, className, size = 28, ...props }, ref) => {
    const controls = useAnimation();
    const isControlledRef = useRef(false);

    useImperativeHandle(ref, () => {
        isControlledRef.current = true;

        return {
            startAnimation: () => controls.start("animate"),
            stopAnimation: () => controls.start("normal"),
        };
    });

    const handleMouseEnter = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseEnter?.(e);
            } else {
                controls.start("animate");
            }
        },
        [controls, onMouseEnter]
    );

    const handleMouseLeave = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseLeave?.(e);
            } else {
                controls.start("normal");
            }
        },
        [controls, onMouseLeave]
    );

    return (
        <div className={cn(className)} onMouseEnter={handleMouseEnter} onMouseLeave={handleMouseLeave} {...props}>
            <svg
                fill="none"
                height={size}
                stroke="currentColor"
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth="1.5"
                viewBox="0 0 24 24"
                width={size}
                xmlns="http://www.w3.org/2000/svg"
            >
                <path d="M6 10.5h2.25a2.25 2.25 0 0 0 2.25-2.25V6a2.25 2.25 0 0 0-2.25-2.25H6A2.25 2.25 0 0 0 3.75 6v2.25A2.25 2.25 0 0 0 6 10.5Zm0 9.75h2.25A2.25 2.25 0 0 0 10.5 18v-2.25a2.25 2.25 0 0 0-2.25-2.25H6a2.25 2.25 0 0 0-2.25 2.25V18A2.25 2.25 0 0 0 6 20.25Zm9.75-9.75H18a2.25 2.25 0 0 0 2.25-2.25V6A2.25 2.25 0 0 0 18 3.75h-2.25A2.25 2.25 0 0 0 13.5 6v2.25a2.25 2.25 0 0 0 2.25 2.25Z" />
                <motion.path animate={controls} d="M16.875 13.5v6.75" initial="normal" variants={VERTICAL_LINE_VARIANT} />
                <motion.path animate={controls} d="M13.5 16.875h6.75" initial="normal" variants={HORIZONTAL_LINE_VARIANT} />
            </svg>
        </div>
    );
});

SquaresPlusIcon.displayName = "SquaresPlusIcon";

export { SquaresPlusIcon };
