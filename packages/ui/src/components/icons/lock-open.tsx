"use client";

import { cn } from "@foundry/ui/lib/utils";
import type { Variants } from "motion/react";
import { motion, useAnimation } from "motion/react";
import type { HTMLAttributes } from "react";
import { forwardRef, useCallback, useImperativeHandle, useRef } from "react";

export interface LockOpenIconHandle {
    startAnimation: () => void;
    stopAnimation: () => void;
}

interface LockOpenIconProps extends HTMLAttributes<HTMLDivElement> {
    size?: number;
}

const SVG_VARIANTS: Variants = {
    normal: {
        scale: 1,
    },
    animate: {
        scale: [1, 1.05, 1],
        transition: {
            duration: 0.4,
            ease: "easeInOut",
        },
    },
};

const SHACKLE_VARIANTS: Variants = {
    normal: {
        y: 0,
    },
    animate: {
        y: [0, -1.25, 0],
        transition: {
            duration: 0.4,
            ease: "easeOut",
        },
    },
};

const LockOpenIcon = forwardRef<LockOpenIconHandle, LockOpenIconProps>(({ onMouseEnter, onMouseLeave, className, size = 20, ...props }, ref) => {
    const controls = useAnimation();
    const isControlledRef = useRef(false);

    useImperativeHandle(ref, () => {
        isControlledRef.current = true;

        return {
            startAnimation: () => controls.start("animate"),
            stopAnimation: () => controls.start("normal"),
        };
    });

    const handleMouseEnter = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseEnter?.(e);
            } else {
                controls.start("animate");
            }
        },
        [controls, onMouseEnter]
    );

    const handleMouseLeave = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseLeave?.(e);
            } else {
                controls.start("normal");
            }
        },
        [controls, onMouseLeave]
    );

    return (
        <div className={cn(className)} onMouseEnter={handleMouseEnter} onMouseLeave={handleMouseLeave} {...props}>
            <motion.svg
                animate={controls}
                fill="none"
                height={size}
                initial="normal"
                stroke="currentColor"
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth="1.5"
                variants={SVG_VARIANTS}
                viewBox="0 0 24 24"
                width={size}
                xmlns="http://www.w3.org/2000/svg"
            >
                <motion.path
                    animate={controls}
                    d="M13.5 10.5V6.75C13.5 4.26472 15.5147 2.25 18 2.25C20.4853 2.25 22.5 4.26472 22.5 6.75V10.5"
                    initial="normal"
                    variants={SHACKLE_VARIANTS}
                />
                <path d="M3.75 21.75H14.25C15.4926 21.75 16.5 20.7426 16.5 19.5V12.75C16.5 11.5074 15.4926 10.5 14.25 10.5H3.75C2.50736 10.5 1.5 11.5074 1.5 12.75V19.5C1.5 20.7426 2.50736 21.75 3.75 21.75Z" />
            </motion.svg>
        </div>
    );
});

LockOpenIcon.displayName = "LockOpenIcon";

export { LockOpenIcon };
