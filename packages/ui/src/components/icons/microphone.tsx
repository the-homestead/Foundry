"use client";

import { cn } from "@foundry/ui/lib/utils";
import type { Variants } from "motion/react";
import { motion, useAnimation } from "motion/react";
import type { HTMLAttributes } from "react";
import { forwardRef, useCallback, useImperativeHandle, useRef } from "react";

export interface MicrophoneIconHandle {
    startAnimation: () => void;
    stopAnimation: () => void;
}

interface MicrophoneIconProps extends HTMLAttributes<HTMLDivElement> {
    size?: number;
}

const CAPSULE_VARIANTS: Variants = {
    normal: { y: 0 },
    animate: {
        y: [0, -3, 0, -2, 0],
        transition: {
            duration: 0.6,
            ease: "easeInOut",
        },
    },
};

const MicrophoneIcon = forwardRef<MicrophoneIconHandle, MicrophoneIconProps>(({ onMouseEnter, onMouseLeave, className, size = 20, ...props }, ref) => {
    const controls = useAnimation();
    const isControlledRef = useRef(false);

    useImperativeHandle(ref, () => {
        isControlledRef.current = true;

        return {
            startAnimation: () => controls.start("animate"),
            stopAnimation: () => controls.start("normal"),
        };
    });

    const handleMouseEnter = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseEnter?.(e);
            } else {
                controls.start("animate");
            }
        },
        [controls, onMouseEnter]
    );

    const handleMouseLeave = useCallback(
        (e: React.MouseEvent<HTMLDivElement>) => {
            if (isControlledRef.current) {
                onMouseLeave?.(e);
            } else {
                controls.start("normal");
            }
        },
        [controls, onMouseLeave]
    );

    return (
        <div className={cn(className)} onMouseEnter={handleMouseEnter} onMouseLeave={handleMouseLeave} {...props}>
            <svg
                fill="none"
                height={size}
                overflow="visible"
                stroke="currentColor"
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth="1.5"
                viewBox="0 0 24 24"
                width={size}
                xmlns="http://www.w3.org/2000/svg"
            >
                <path d="M12 18.75a6 6 0 0 0 6-6v-1.5m-6 7.5a6 6 0 0 1-6-6v-1.5m6 7.5v3.75m-3.75 0h7.5" />
                <motion.g animate={controls} initial="normal" variants={CAPSULE_VARIANTS}>
                    <path d="M12 15.75a3 3 0 0 1-3-3V4.5a3 3 0 1 1 6 0v8.25a3 3 0 0 1-3 3Z" />
                </motion.g>
            </svg>
        </div>
    );
});

MicrophoneIcon.displayName = "MicrophoneIcon";

export { MicrophoneIcon };
